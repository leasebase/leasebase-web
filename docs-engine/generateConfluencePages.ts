import type { FlowDiagram } from "./extractFlows.js";
import type { UseCase } from "./extractUseCases.js";

/* ── Types ────────────────────────────────────────────────────── */

export interface ConfluencePage {
  title: string;
  /** If set, this page will be created as a child of the page with this title. */
  parentTitle?: string;
  /** Confluence storage format HTML body. */
  body: string;
}

/* ── Reusable HTML fragments ─────────────────────────────────── */

function footer(): string {
  return `<hr/><p><em>Auto-generated by LeaseBase docs-engine · Commit: <code>${escapeHtml(getCommitSha())}</code> · ${escapeHtml(nowISO())}</em></p>`;
}

function h1(text: string): string {
  return `<h1>${escapeHtml(text)}</h1>`;
}
function h2(text: string): string {
  return `<h2>${escapeHtml(text)}</h2>`;
}
function h3(text: string): string {
  return `<h3>${escapeHtml(text)}</h3>`;
}

function ul(items: string[]): string {
  if (items.length === 0) return "<p><em>None</em></p>";
  return "<ul>" + items.map((i) => `<li>${escapeHtml(i)}</li>`).join("") + "</ul>";
}

function ol(items: string[]): string {
  if (items.length === 0) return "<p><em>None</em></p>";
  return "<ol>" + items.map((i) => `<li>${escapeHtml(i)}</li>`).join("") + "</ol>";
}

function table(headers: string[], rows: string[][]): string {
  const thead = "<thead><tr>" + headers.map((h) => `<th>${escapeHtml(h)}</th>`).join("") + "</tr></thead>";
  const tbody =
    "<tbody>" +
    rows.map((row) => "<tr>" + row.map((cell) => `<td>${cell}</td>`).join("") + "</tr>").join("") +
    "</tbody>";
  return `<table>${thead}${tbody}</table>`;
}

function mermaidBlock(code: string): string {
  return confluenceMacro("code", code, { language: "text", title: "Mermaid Diagram" });
}

function infoPanel(body: string): string {
  return `<ac:structured-macro ac:name="info" ac:schema-version="1"><ac:rich-text-body>${body}</ac:rich-text-body></ac:structured-macro>`;
}

