function generateUserFlowsPage(flows: FlowDiagram[]): ConfluencePage {
  const sections = flows.map((flow) =>
    [
      h2(flow.title),
      `<p>${escapeHtml(flow.description)}</p>`,
      mermaidBlock(flow.mermaid),
    ].join("\n"),
  );

  return {
    title: "User Flows",
    parentTitle: "LeaseBase Web Documentation",
    body: [h1("User Flows"), ...sections, footer()].join("\n"),
  };
}

function generateUseCasesPage(useCases: UseCase[]): ConfluencePage {
  // Group by category
  const categories = new Map<string, UseCase[]>();
  for (const uc of useCases) {
    if (!categories.has(uc.category)) categories.set(uc.category, []);
    categories.get(uc.category)!.push(uc);
  }

  const sections: string[] = [];
  for (const [category, cases] of categories) {
    sections.push(h2(category));
    for (const uc of cases) {
      sections.push(
        h3(uc.title),
        table(
          ["Field", "Value"],
          [
            ["Actors", uc.actors.join(", ")],
            ["Entry Point", `<code>${escapeHtml(uc.entryPoint)}</code>`],
            ["Preconditions", ul(uc.preconditions)],
            ["Steps", ol(uc.steps)],
            ["Alternate Flows", ul(uc.alternateFlows)],
            ["Errors", ul(uc.errors)],
            ["Permissions", uc.permissions.join(", ")],
            ["API Dependencies", uc.apiDependencies.length ? uc.apiDependencies.map((a) => `<code>${escapeHtml(a)}</code>`).join(", ") : "<em>None detected</em>"],
          ],
        ),
      );
    }
  }

  return {
    title: "Use Cases",
    parentTitle: "LeaseBase Web Documentation",
    body: [h1("Use Cases"), ...sections, footer()].join("\n"),
  };
}

function generatePageReferencePage(routes: RouteInfo[], permissions: PermissionsMatrix): ConfluencePage {
  const activeRoutes = routes.filter((r) => !r.isRedirect);

  const sections = activeRoutes.map((route) => {
    const permEntry = permissions.entries.find((e) => e.route === route.route);
    const personas = permEntry?.personas || [];

    return [
      h3(permEntry?.label || route.route),
      table(
        ["Field", "Value"],
        [
          ["Route", `<code>${escapeHtml(route.route)}</code>`],
          ["File", `<code>${escapeHtml(route.filePath)}</code>`],
          ["Purpose", route.requiresAuth ? "Authenticated app page" : "Public page"],
          ["Components", route.components.length ? route.components.map((c) => `<code>${escapeHtml(c)}</code>`).join(", ") : "<em>None imported</em>"],
          ["Actions / Navigation", route.navigationTargets.length ? route.navigationTargets.map((t) => `<code>${escapeHtml(t)}</code>`).join(", ") : "<em>None</em>"],
          ["API Dependencies", route.apiCalls.length ? route.apiCalls.map((a) => `<code>${escapeHtml(a)}</code>`).join(", ") : "<em>None</em>"],
          ["Personas", personas.length ? personas.join(", ") : "<em>All</em>"],
        ],
      ),
    ].join("\n");
  });

  return {
    title: "Page Reference",
    parentTitle: "LeaseBase Web Documentation",
    body: [h1("Page Reference"), ...sections, footer()].join("\n"),
  };
}

function generatePermissionsMatrixPage(permissions: PermissionsMatrix): ConfluencePage {
  const personas = permissions.personas;
  const headers = ["Route", "Label", ...personas];
  const rows = permissions.entries.map((entry) => [
    `<code>${escapeHtml(entry.route)}</code>`,
    escapeHtml(entry.label) + (entry.isFuture ? " <em>(future)</em>" : ""),
    ...personas.map((p) => (entry.personas.includes(p) ? "✅" : "—")),
  ]);

  return {
    title: "Permissions Matrix",
    parentTitle: "LeaseBase Web Documentation",
    body: [
      h1("Permissions Matrix"),
      "<p>Matrix showing which personas can access each route.</p>",
      table(headers, rows),
      footer(),
    ].join("\n"),
  };
}

