function generateTestingScenariosPage(routes: RouteInfo[], useCases: UseCase[]): ConfluencePage {
  const scenarios: string[] = [];

  // Auth scenarios
  scenarios.push(h2("Authentication Tests"));
  scenarios.push(
    table(
      ["Scenario", "Steps", "Expected Result"],
      [
        ["Login with valid credentials", "Enter valid email/password, click Sign In", "Redirect to /app dashboard"],
        ["Login with invalid credentials", "Enter wrong password, click Sign In", "Error message shown"],
        ["Login with unverified email", "Enter unverified user, click Sign In", "Prompted to verify email"],
        ["Register new user", "Fill all fields correctly, submit", "Redirect to email verification"],
        ["Register with mismatched passwords", "Enter different passwords", "Client-side error shown"],
        ["Verify email with valid code", "Enter correct 6-digit code", "Redirect to login with success"],
        ["Resend verification code", "Click Resend Code", "Success message shown"],
        ["Dev bypass login", "Select role, click dev bypass", "Redirect to /app (dev only)"],
      ],
    ),
  );

  // Navigation scenarios
  scenarios.push(h2("Navigation Tests"));
  scenarios.push(
    table(
      ["Scenario", "Steps", "Expected Result"],
      [
        ["Unauthenticated access to /app", "Navigate to /app without login", "Redirect to /auth/login"],
        ["Dashboard persona routing", "Login as PM, Owner, or Tenant", "Correct dashboard variant shown"],
        ["Sidebar navigation", "Click each nav item", "Correct page loads, active state shown"],
        ["Mobile menu toggle", "Open/close hamburger menu", "Menu opens/closes with animation"],
        ["Breadcrumb navigation", "Navigate to nested page", "Breadcrumbs show correct path"],
      ],
    ),
  );

  // Per-use-case scenarios
  scenarios.push(h2("Use Case Scenarios"));
  const ucRows = useCases.map((uc) => [
    escapeHtml(uc.title),
    uc.steps[0] || "",
    "Page loads successfully with correct content",
  ]);
  scenarios.push(table(["Use Case", "Entry Step", "Expected"], ucRows));

  return {
    title: "Testing Scenarios",
    parentTitle: "LeaseBase Web Documentation",
    body: [h1("Testing Scenarios"), ...scenarios, footer()].join("\n"),
  };
}

/* ── Public API ───────────────────────────────────────────────── */

export function generateConfluencePages(
  routes: RouteInfo[],
  permissions: PermissionsMatrix,
  flows: FlowDiagram[],
  useCases: UseCase[],
): ConfluencePage[] {
  return [
    generateRootPage(),
    generateOverviewPage(routes, permissions),
    generateUserFlowsPage(flows),
    generateUseCasesPage(useCases),
    generatePageReferencePage(routes, permissions),
    generatePermissionsMatrixPage(permissions),
    generateErrorHandlingPage(),
    generateTestingScenariosPage(routes, useCases),
  ];
}
